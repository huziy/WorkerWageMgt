    public void refreshTable() {
        int rowCount = workerTable.getRowCount();
        DefaultTableModel model = (DefaultTableModel) workerTable.getModel();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        model.setRowCount(0);
        for (Worker w : workerDirectory.getWorkerlist()) {
            Object row[] = new Object[1];
            row[0] = w;

            model.addRow(row);
        }
    }

