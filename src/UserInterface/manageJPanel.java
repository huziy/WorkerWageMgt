    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed

        int row = workerTable.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "请选择要删除的工人", "提示", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Worker w = (Worker) workerTable.getValueAt(row, 0);
        workerDirectory.removeWorker(w);
        refreshTable();
        JOptionPane.showMessageDialog(null, "删除成功", "提示", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnRemoveActionPerformed

